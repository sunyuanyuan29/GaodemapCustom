public class NavigationSettingBean {
    String fangxiang;
    String dayModel;
    boolean traffic;
    boolean vocie;
    boolean qianyinLine;
    boolean lukuang;

    public String getFangxiang() {
        return fangxiang;
    }

    public void setFangxiang(String fangxiang) {
        this.fangxiang = fangxiang;
    }

    public String getDayModel() {
        return dayModel;
    }

    public void setDayModel(String dayModel) {
        this.dayModel = dayModel;
    }

    public boolean getTraffic() {
        return traffic;
    }

    public void setTraffic(boolean traffic) {
        this.traffic = traffic;
    }

    public boolean getVocie() {
        return vocie;
    }

    public void setVocie(boolean vocie) {
        this.vocie = vocie;
    }

    public boolean getQianyinLine() {
        return qianyinLine;
    }

    public void setQianyinLine(boolean qianyinLine) {
        this.qianyinLine = qianyinLine;
    }

    public boolean getLukuang() {
        return lukuang;
    }

    public void setLukuang(boolean lukuang) {
        this.lukuang = lukuang;
    }
}
